package com.java6.examples.clone;

public class DOB {
	Integer month;
	Integer year;

	public DOB(Integer month, Integer year) {
		super();
		this.month = month;
		this.year = year;
	}

	public Integer getMonth() {
		return month;
	}

	public void setMonth(Integer month) {
		this.month = month;
	}

	public Integer getYear() {
		return year;
	}

	public void setYear(Integer year) {
		this.year = year;
	}

}
